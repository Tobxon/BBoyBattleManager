b3m::gui::presentation::SlideSelector::SlideSelector(QWidget* i_parent)
	: QPushButton(i_parent)
{
	connect(this, &QPushButton::clicked, [this](){
		emit newSlide(getSlide());
		setEnabled(false);
	});
}


